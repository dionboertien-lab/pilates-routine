export function attachBottomNavListeners() {
  document.querySelectorAll('[data-nav-target]').forEach(btn => {
    btn.addEventListener('click', (e) => {
      const target = e.currentTarget.getAttribute('data-nav-target');
      state.screen = target;
      render();
    });
  });
}
